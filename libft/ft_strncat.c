#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*str;

	str = s1;
	while (*s1 != 0)
		s1++;
	while (n-- && *s2 != 0)
		*s1++ = *s2++;
	*s1 = '\0';
	return (str);
}
