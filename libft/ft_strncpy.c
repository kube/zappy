#include <string.h>

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*s;

	s = s1;
	while (n > 0 && *s2)
	{
		*s1++ = *s2++;
		n--;
	}
	while (n > 0)
	{
		*s1++ = '\0';
		n--;
	}
	return (s);
}
